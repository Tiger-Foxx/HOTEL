    private void BtnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSupprimerActionPerformed
        // TODO add your handling code here:
        int selectedRow;
        int dialogResult=JOptionPane.showConfirmDialog(null, "voulez vous vraiment supprimer ce client ?","Confirmation",JOptionPane.YES_OPTION);
        int selectedIndivID;
        try {
            System.out.println(TableClient.getValueAt(TableClient.getSelectedRow(), 0).getClass().getSimpleName());
            selectedIndivID =(int)TableClient.getValueAt(TableClient.getSelectedRow(), 0);
            if(dialogResult==JOptionPane.YES_OPTION){
                selectedRow=TableClient.getSelectedRow();
                model.removeRow(selectedRow);
                gcli.Supprimer(selectedIndivID);
                ActualiserTableIndiv();
                TableIndiv();
                ActualiserTableIndiv();
                TableIndiv();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "OUPS!! une erreur s'est produite");
        }
    }//GEN-LAST:event_BtnSupprimerActionPerformed

